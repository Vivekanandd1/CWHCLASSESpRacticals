package _2_Operators;

public class _3_ComparisonOperator {
	//comparison Operator always return a boolean value in its output.

	public static void main(String[] args) {
		int x = 7;
		int y = 8;
		System.out.println(63>8);
		System.out.println(x==y);
		System.out.println(y>=x);
		System.out.println(65<=91);
	}
}
