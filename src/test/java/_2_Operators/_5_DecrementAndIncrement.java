package _2_Operators;

public class _5_DecrementAndIncrement {

	public static void main(String[] args) {
		 int a = 7;
		 int b = 8;
		 float x = 16 +0.5f;
		 int y = 8;
		 
		 System.out.println(++a);//in this first it incrementing the value and after that storing it. 
		 System.out.println(a++);//in this first it Storing the value and after that incrementing it. 
		 System.out.println(a);// here we can see the result from line 12 it got incremented
		 System.out.println(--b);
		 System.out.println(b--);
		 System.out.println(b);
		 System.out.println(x);
		 System.out.println(y++*8);//++ having more precedence compare to *
		 
		 
		 
		 
	}
}
