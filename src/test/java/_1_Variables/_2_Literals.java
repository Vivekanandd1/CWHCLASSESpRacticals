package _1_Variables;

public class _2_Literals {
    public static void main(String[] args) {
    	
    	byte age = 34;
        int age2 = 56;
        short age3 = 87;
        long ageDino = 5666666666666L;
        char ch = 'A';
        float f1 = 5.6f;
        double d1 = 4.66;
        boolean a = true;
        String str = "Harry";
        System.out.println(str);
        
        /*
         * 101 – Integer literal
          10.1f – float literal
          10.1 – double literal (default type for decimals)
          ‘A’ – character literal
          true – Boolean literal
          “Harry” – String literal
         */
	
}
}
